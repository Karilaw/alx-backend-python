#!/usr/bin/env python3
"""
This module provides a function

The function `add` takes two arguments
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers.

    Parameters:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of the two numbers.
    """
    return a + b
