#!/usr/bin/env python3
"""
This module provides a function to concatenate
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings.

    Parameters:
    str1 (str): The first string.
    str2 (str): The second string.

    Returns:
    str: The concatenated string.
    """
    return str1 + str2
